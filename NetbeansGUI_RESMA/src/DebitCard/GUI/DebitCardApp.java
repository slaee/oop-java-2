    private void jBtnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnWithdrawActionPerformed
        // TODO add your handling code here:
        float amtDeposit = 0.00F;
        try {
            amtDeposit = Float.parseFloat(jTxtAmtField.getText());
            //jLblAmtErrMessage.setText("");
            
            if(account.withdrawCash(amtDeposit)){
                jLblNewBalance.setText(String.format("%.2f", account.getBalance()));
                jLblAmtErrMessage.setText("");
            } else {
                jLblAmtErrMessage.setText("Insufficient balance");
            }
        } catch (Exception e){
            jLblAmtErrMessage.setText("Invalid input");
        }
    }//GEN-LAST:event_jBtnWithdrawActionPerformed

