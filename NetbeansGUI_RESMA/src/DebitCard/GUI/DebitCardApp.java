    private void jBtnCreateAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCreateAccActionPerformed
        // TODO add your handling code here:
        
        String accountNumber = jTxtAccNumField.getText();
        String[] name = jTxtAccNameField.getText().split(" ");
        
        String firstName = "";
        String middleName = "";
        String lastName = "";
                
        float balance = 0.00F;
        
        
        boolean isValidAccNum = false;
        if(!jTxtAccNumField.getText().equals("")){
            isValidAccNum = true;
            jLblAccErrMessage.setText("");
        } else {
            jLblAccErrMessage.setText("Invalid input");
        }
        
        boolean isValidName = false;
        try {
            firstName = name[0];
            middleName = name[1];
            lastName = name[2];
            isValidName = true;
            jLblNameErrMessage.setText("");
        } catch (Exception e){
            jLblNameErrMessage.setText("Invalid input");
        }
        
        boolean isValidBalance = false;
        try {
            balance = Float.parseFloat(jTxtBalField.getText());
            isValidBalance = true;
            jLblBalErrMessage.setText("");
        } catch (Exception e){
            jLblBalErrMessage.setText("Invalid input");
        }
        
        if(isValidAccNum && isValidName && isValidBalance){
            account = new DebitCard(accountNumber, new Name(firstName, middleName, lastName), balance);
            jTxtAccNumField.setEnabled(false);
            jTxtAccNameField.setEnabled(false);
            jTxtBalField.setEnabled(false);
            jBtnCreateAcc.setEnabled(false);
            jLblNewBalance.setText(String.format("%.2f", account.getBalance()));
        }
    }//GEN-LAST:event_jBtnCreateAccActionPerformed
    
